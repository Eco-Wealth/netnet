"use client";

import { useMemo, useState } from "react";
import { toInsightTitle } from "@/lib/insight";
import { Button, Input } from "@/components/ui";

async function getJSON<T>(url: string): Promise<T> {
  const res = await fetch(url, { cache: "no-store" });
  return res.json();
}
async function postJSON<T>(url: string, body: any): Promise<T> {
  const res = await fetch(url, { method: "POST", headers: { "content-type": "application/json" }, body: JSON.stringify(body) });
  return res.json();
}

export default function EconomicsLoopClient() {
  const [days, setDays] = useState(7);
  const [usd, setUsd] = useState(100);
  const [out, setOut] = useState<any>(null);
  const [err, setErr] = useState<string | null>(null);

  const can = useMemo(() => days >= 1 && usd >= 1, [days, usd]);

  async function run() {
    setErr(null);
    setOut(null);
    try {
      const revenue = await getJSON<any>(`/api/agent/revenue?action=report&days=${days}`);
      const allocation = await postJSON<any>("/api/agent/allocate", { action: "plan", usd });
      const retireIntent = {
        kind: "retire.intent.v1",
        usd,
        note: "Intent only. Use /retire flow for execution.",
        requiresApproval: true,
      };
      const proof = {
        kind: "netnet.proof.v1",
        subject: { type: "economics.loop", windowDays: days },
        refs: {},
        claims: { revenue, allocation, retireIntent },
        createdAt: new Date().toISOString(),
      };
      setOut({ ok: true, revenue, allocation, retireIntent, proof });
    } catch (e: any) {
      setErr(e?.message || "loop_failed");
    }
  }

  async function createWork() {
    if (!out?.ok) return;
    setErr(null);
    try {
      const title = `Economics loop (${days}d): $${usd} allocation`;
      const res = await postJSON<any>("/api/work", {
        title,
        description: "Generated by Economics Loop (proposal-only). Attach proof/packet outputs and approve next steps.",
        tags: ["economics", "allocation", "retire-intent"],
        priority: "HIGH",
      });
      setOut({ ...out, work: res });
    } catch (e: any) {
      setErr(e?.message || "work_failed");
    }
  }

  return (
    <div className="nn-surface grid gap-3">
      <div className="grid gap-2 sm:grid-cols-3">
        <label className="grid gap-1 text-sm">
          <span className="opacity-70">Revenue window (days)</span>
          <Input
            type="number"
            value={days}
            min={1}
            max={90}
            onChange={(e) => setDays(Number(e.target.value))}
          />
        </label>
        <label className="grid gap-1 text-sm">
          <span className="opacity-70">Plan amount (USD)</span>
          <Input
            type="number"
            value={usd}
            min={1}
            max={1_000_000}
            onChange={(e) => setUsd(Number(e.target.value))}
          />
        </label>
        <div className="flex items-end gap-2">
          <Button
            className="w-full"
            onClick={run}
            disabled={!can}
            title={toInsightTitle({
              what: "Run the economics planning loop.",
              when: "After setting revenue window and planning amount.",
              requires: "No execution approval; proposal mode. Read and proposal API calls only.",
              output: "Revenue report, allocation packet, retire intent, and proof payload.",
            })}
          >
            Run loop
          </Button>
        </div>
      </div>

      <div className="flex flex-wrap gap-2">
        <Button
          variant="ghost"
          onClick={createWork}
          disabled={!out?.ok}
          title={toInsightTitle({
            what: "Create a work item from loop output.",
            when: "After a successful loop run.",
            requires: "Loop output available. Single work API write.",
            output: "Tracked work item for operator review.",
          })}
        >
          Create work item
        </Button>
      </div>

      {err ? <div className="text-sm text-red-300">{err}</div> : null}
      {out ? (
        <pre className="max-h-[420px] overflow-auto rounded-[11px] border border-white/14 bg-black/30 p-3 text-xs">
          {JSON.stringify(out, null, 2)}
        </pre>
      ) : (
        <div className="text-xs opacity-60">Outputs include revenue report, allocation packet, retire intent, and proof.</div>
      )}
    </div>
  );
}
